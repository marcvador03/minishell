#include "minishell.h"

int	ft_env(char **args, char ***env)
{
	int	i;

	if (args[0] == NULL)
		return (-1);
	i = 0;
	while ((*env)[i] != NULL)
	{
		printf("%s\n", (*env)[i]);
		i++;
	}
	return (0);
}
